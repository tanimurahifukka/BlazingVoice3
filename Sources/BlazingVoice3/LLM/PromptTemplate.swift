- 各項目は「・」で始め、1行に収める
- 情報の抜け漏れがないように注意する
- 時系列順に整理する
- 該当しない項目は省略する
- 複数主訴がある場合は主訴ごとにまとめて記載する
- 指導内容（生活指導・薬剤説明・病態説明）は独立した項目として必ず記載する
"""

